select case when I18ITM.NME is null then ITM10.NME else I18ITM.NME end as ITM10NME, SELR22.DBCR as SELR22DBCR,
TXCT22.IID as TXCT22IID, TXCT22.NME as TXCT22NME, TXCT22.AGRT as TXCT22AGRT,
DBCR31.NME as DBCR31NME, DBCR31.IID as DBCR31IID, ITM10.IID as ITM10IID, ITMPRI.PRICT, ITMPRI.PRI, ITMPRI.UNST
from :TITMPRI as ITMPRI 
join :TITM as ITM10 on ITMPRI.ITM=ITM10.IID
join SESEL as SELR22 on ITM10.SELR=SELR22.DBCR
join DBCR as DBCR31 on ITM10.SELR=DBCR31.IID
left join TXCT as TXCT22 on TXCT22.IID=ITM10.TXCT
left join (select NME, HASNM from :TI18ITM where HASNM=:ITMID and LNG=':LNG') as I18ITM on I18ITM.HASNM=ITM10.IID
where ITM=:ITMID;
